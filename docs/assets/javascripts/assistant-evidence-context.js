(() => {
  "use strict";

  const root = document.querySelector("[data-mission-control]");
  const output = document.querySelector("[data-narrative-output]");
  const action = document.querySelector("[data-generate-narrative]");
  if (
    !(root instanceof HTMLElement) ||
    !(output instanceof HTMLElement) ||
    !(action instanceof HTMLButtonElement)
  ) {
    return;
  }

  let selectedQuestion = "What are the highest-severity findings?";
  let missionPromise;

  const isRecord = (value) =>
    value !== null && typeof value === "object" && !Array.isArray(value);

  const create = (tagName, className = "", text = "") => {
    const node = document.createElement(tagName);
    if (className) node.className = className;
    if (text) node.textContent = text;
    return node;
  };

  const formatValue = (value) => {
    if (typeof value === "string") return value;
    if (value === null || value === undefined) return "not available";
    if (Array.isArray(value)) return value.map(formatValue).join(" / ");
    if (isRecord(value)) return JSON.stringify(value);
    return String(value);
  };

  const loadMission = async () => {
    const response = await fetch("/assets/data/mission-control.json", {
      credentials: "same-origin",
      headers: { Accept: "application/json" },
    });
    if (
      !response.ok ||
      !response.headers.get("Content-Type")?.includes("application/json")
    ) {
      throw new Error("the Mission package could not be loaded");
    }
    const mission = await response.json();
    if (
      !isRecord(mission) ||
      mission.schema_version !== "2.0.0" ||
      typeof mission.snapshot_id !== "string" ||
      !Array.isArray(mission.findings)
    ) {
      throw new Error("the Mission package failed the evidence-panel contract");
    }
    return mission;
  };

  const frameworkLabels = {
    cis_benchmark: "CIS Level 1",
    stig: "STIG",
    nist_800_171r3: "NIST SP 800-171",
    nist_800_53r5: "NIST SP 800-53",
    cmmc: "CMMC",
  };

  const deterministicChange = (finding) => {
    const key =
      typeof finding.setting_key === "string"
        ? finding.setting_key
        : finding.rule_id;
    const expected = formatValue(finding.expected_value);
    const observed = formatValue(finding.observed_value);
    switch (finding.drift_type) {
      case "Value drift":
        return `Set ${key} to ${expected} in the approved Intune policy (observed: ${observed}), then re-collect and review assignment evidence.`;
      case "Assignment drift":
        return `Keep ${key} at ${expected}; assign the approved policy to the intended scope, review exclusions, then re-collect.`;
      case "Conflicting policy":
        return `Resolve overlapping Intune policies so the effective value of ${key} is ${expected} (observed conflict: ${observed}), then re-collect.`;
      case "Missing from tenant":
        return `Create or identify an approved Intune policy expressing ${key} = ${expected}, assign it to the intended scope, then re-collect.`;
      default:
        return typeof finding.remediation_guidance === "string"
          ? finding.remediation_guidance
          : "Human review is required before changing Intune.";
    }
  };

  const addDetail = (list, label, value, code = false) => {
    const term = create("dt", "", label);
    const description = create("dd");
    description.append(create(code ? "code" : "span", "", value));
    list.append(term, description);
  };

  const renderFileVaultContext = async () => {
    if (output.querySelector("[data-deterministic-assistant-context]")) return;

    const panel = create("section", "runtime-evidence-context");
    panel.dataset.deterministicAssistantContext = "filevault";
    panel.append(
      create("h4", "", "Deterministic FileVault setting context"),
      create(
        "p",
        "runtime-evidence-context-lead",
        "This context comes directly from the fingerprint-verified public package; it is not generated by AI.",
      ),
    );

    try {
      const mission = await (missionPromise ??= loadMission());
      const findings = mission.findings.filter(
        (finding) =>
          isRecord(finding) &&
          typeof finding.rule_id === "string" &&
          finding.rule_id.includes("filevault"),
      );

      if (!findings.length) {
        panel.append(
          create(
            "p",
            "",
            "No FileVault drift finding is present. Open the settings matrix to distinguish aligned, unsupported, and missing evidence states.",
          ),
        );
      }

      for (const finding of findings) {
        const detail = create("article", "runtime-evidence-card");
        detail.append(
          create(
            "h5",
            "",
            typeof finding.title === "string"
              ? finding.title
              : "FileVault requirement",
          ),
        );
        const list = create("dl", "runtime-evidence-list");
        addDetail(
          list,
          "Setting",
          typeof finding.setting_key === "string"
            ? finding.setting_key
            : finding.rule_id,
          true,
        );
        addDetail(list, "Observed", formatValue(finding.observed_value), true);
        addDetail(
          list,
          "Approved target",
          formatValue(finding.expected_value),
          true,
        );
        addDetail(
          list,
          "Technical state",
          typeof finding.drift_type === "string"
            ? finding.drift_type
            : "not available",
        );
        addDetail(
          list,
          "Assignment evidence",
          typeof finding.assignment_summary === "string"
            ? finding.assignment_summary
            : "not available",
        );

        const mappings = isRecord(finding.mapped_controls)
          ? finding.mapped_controls
          : {};
        for (const [key, label] of Object.entries(frameworkLabels)) {
          const identifiers = Array.isArray(mappings[key])
            ? mappings[key].filter((value) => typeof value === "string")
            : [];
          addDetail(
            list,
            label,
            identifiers.length ? identifiers.join(", ") : "No reviewed mapping",
            identifiers.length > 0,
          );
        }
        addDetail(
          list,
          "CIS Level 2",
          "Baseline not loaded; EvidenceOps does not infer Level 2 coverage",
        );
        addDetail(
          list,
          "Required technical change",
          deterministicChange(finding),
        );
        addDetail(
          list,
          "Public-safe evidence",
          Array.isArray(finding.source_evidence_ids)
            ? finding.source_evidence_ids
                .filter((value) => typeof value === "string")
                .join(", ") || "No linked setting evidence"
            : "No linked setting evidence",
          true,
        );
        detail.append(list);
        panel.append(detail);
      }

      const link = create(
        "a",
        "md-button",
        "Open the complete settings matrix",
      );
      link.href = "../settings-matrix/";
      panel.append(link);
    } catch (error) {
      panel.append(
        create(
          "p",
          "",
          error instanceof Error
            ? `Deterministic context unavailable: ${error.message}`
            : "Deterministic context unavailable",
        ),
      );
    }

    output.append(panel);
  };

  for (const button of document.querySelectorAll("[data-question]")) {
    if (!(button instanceof HTMLButtonElement)) continue;
    button.addEventListener("click", () => {
      selectedQuestion = button.dataset.question || selectedQuestion;
      delete output.dataset.contextQuestion;
    });
  }

  const maybeRender = () => {
    if (
      output.hidden ||
      !/filevault|encryption/i.test(selectedQuestion) ||
      output.dataset.contextQuestion === selectedQuestion
    ) {
      return;
    }
    output.dataset.contextQuestion = selectedQuestion;
    void renderFileVaultContext();
  };

  action.addEventListener("click", () => {
    delete output.dataset.contextQuestion;
  });

  const observer = new MutationObserver(maybeRender);
  observer.observe(output, {
    attributes: true,
    attributeFilter: ["hidden"],
    childList: true,
  });
})();
